int	ft_nbrlen(int nb)
{
	int	i;

	i = 0;
	if (nb == 0)
		return (0);
	if (nb < 0)
		nb *= -1;
	while (nb)
	{
		nb /= 10;
		i++;
	}
	return (i);
}
