#include <string.h>

char	*ft_strchr(const char *s, int c)
{
	int	i;

	c %= 256;
	i = 0;
	while (s[i] || c == 0)
	{
		if (s[i] == c)
			return ((char *)&s[i]);
		i++;
	}
	return (NULL);
}
